#include "main.h"
#include <string.h>

/**
 * Description: _strncpy - copies a string 'src' along with null termination
 * to 'dest' poiter
 * @dest: pointer to be copied to
 * @src: pointer carrying the string to be pointed to
 * @n: is maximum number of chars to be pointed to
 * Return: returns the pointer containg the copied string
 */

char *_strncpy(char *dest, char *src, int n)
{
return ((dest, src, n));
}
