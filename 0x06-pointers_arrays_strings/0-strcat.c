/**
 * Description: *_strcat - function concatinates two strings
 * @dest: is a string to be appended on
 * @src: is a string that will be appending on another
 * Return: returns a pointer to the concatinated string
 */

char *_strcat(char *dest, char *src)
{
return (strcat(dest, src));
}
